    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

        if (!this.txtnombre.getText().isEmpty() && !this.txtdni.getText().isEmpty() && !this.txtdireccion.getText().isEmpty() && !this.txttelefono.getText().isEmpty() && !this.txtfecha.getText().isEmpty()) {
            String nombre = this.txtnombre.getText();
            String dni = this.txtdni.getText();
            String direccion = this.txtdireccion.getText();
            String telefono = this.txttelefono.getText();
            String fechaString = this.txtfecha.getText();

            long fechanac = Date.parse(fechaString);

            Date fechaDate1 = new Date(fechanac);

            cliente.agregarCliente(nombre, dni, direccion, telefono, fechaDate1);
            

            this.tableclientes.setModel(cliente.mostrarClientes());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes en los Datos del cliente ", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        if (!this.txtnombre.getText().isEmpty() && !this.txtdni.getText().isEmpty() && !this.txtdireccion.getText().isEmpty() && !this.txttelefono.getText().isEmpty() && !this.txtfecha.getText().isEmpty()) {
            String nombre = this.txtnombre.getText();
            String dni = this.txtdni.getText();
            String direccion = this.txtdireccion.getText();
            String telefono = this.txttelefono.getText();
            String fechaString = this.txtfecha.getText();

            long fechanac = Date.parse(fechaString);

            Date fechaDate1 = new Date(fechanac);

            int escoger = JOptionPane.showConfirmDialog(null, "Desea realizar esta modificacion del cliente?");
            if (escoger == 0) {
                cliente.moficarCliente(nombre, dni, direccion, telefono, fechaDate1);
                this.tableclientes.setModel(cliente.mostrarClientes());
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes en los Datos del cliente ", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int filaSeleccionada = this.tableclientes.getSelectedRow();

        int escoger = JOptionPane.showConfirmDialog(null, "Desea realizar esta eliminacion del cliente?");
        if (escoger == 0) {
            cliente.eliminarCliente(this.tableclientes.getValueAt(filaSeleccionada, 0).toString());
            this.tableclientes.setModel(cliente.mostrarClientes());
        }

    }//GEN-LAST:event_jButton8ActionPerformed

    private void tableclientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableclientesMouseClicked

        int filaSeleccionada = this.tableclientes.rowAtPoint(evt.getPoint());
        this.txtdni.setText(this.tableclientes.getValueAt(filaSeleccionada, 0).toString());
        this.txtnombre.setText(this.tableclientes.getValueAt(filaSeleccionada, 1).toString());
        this.txtdireccion.setText(this.tableclientes.getValueAt(filaSeleccionada, 2).toString());

        this.txttelefono.setText(this.tableclientes.getValueAt(filaSeleccionada, 3).toString());
        this.txtfecha.setText(this.tableclientes.getValueAt(filaSeleccionada, 4).toString());


    }//GEN-LAST:event_tableclientesMouseClicked

